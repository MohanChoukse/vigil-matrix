export const generateChartData = () => {
  const days = [];
  for (let i = 6; i >= 0; i--) {
    const date = new Date();
    date.setDate(date.getDate() - i);
    
    // Generate realistic data with increasing threat patterns
    const totalPosts = Math.floor(Math.random() * 50) + 30;
    const safePosts = Math.floor(totalPosts * (0.6 + Math.random() * 0.2));
    const suspiciousPosts = Math.floor((totalPosts - safePosts) * (0.4 + Math.random() * 0.3));
    const highlySuspiciousPosts = totalPosts - safePosts - suspiciousPosts;
    
    days.push({
      date: date.toISOString().split('T')[0],
      dateLabel: date.toLocaleDateString('en-US', { month: 'short', day: 'numeric' }),
      safe: safePosts,
      suspicious: suspiciousPosts,
      highlySuspicious: highlySuspiciousPosts,
      total: totalPosts
    });
  }
  return days;
};

export const getTopHashtags = () => {
  const hashtagCounts = {};
  
  mockPosts.forEach(post => {
    if (post.classification !== 'Safe') {
      post.hashtags.forEach(hashtag => {
        hashtagCounts[hashtag] = (hashtagCounts[hashtag] || 0) + 1;
      });
    }
  });
  
  return Object.entries(hashtagCounts)
    .sort((a, b) => (b[1] as number) - (a[1] as number))
    .slice(0, 10)
    .map(([hashtag, count]) => ({ hashtag, count: count as number }));
};